    private void btnBuscarRegresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarRegresoActionPerformed
        this.setCursor(Cursor.WAIT_CURSOR);
        String dt_fecha_salida = dt_salida.getDatePicker().getComponentDateTextField().getText();
        LocalTime dt_hora_salida = dt_salida.getTimePicker().getTime();
        String dt_fecha_regreso = dt_regreso.getDatePicker().getComponentDateTextField().getText();
        LocalTime dt_hora_regreso = dt_regreso.getTimePicker().getTime();
        int selectedIndex = cboOrigen.getSelectedIndex();
        if (selectedIndex <= 0){
            JOptionPane.showMessageDialog(this, "El campo de origen es obligatorio.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
            this.setCursor(Cursor.DEFAULT_CURSOR);
            return;
        } else if (dt_fecha_salida.trim().equals("")){
            JOptionPane.showMessageDialog(this, "El campo de fecha de salida es obligatorio.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
            this.setCursor(Cursor.DEFAULT_CURSOR);
            return;
        } else if (dt_hora_salida == null){
            JOptionPane.showMessageDialog(this, "El campo de hora de salida es obligatorio.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
            this.setCursor(Cursor.DEFAULT_CURSOR);
            return;
        }
        if (chk_redondo.isSelected()) {            
            if (dt_fecha_regreso.trim().equals("")){
                JOptionPane.showMessageDialog(this, "El campo de fecha de regreso es obligatorio.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
                this.setCursor(Cursor.DEFAULT_CURSOR);
                return;
            } else if (dt_hora_regreso == null){
                JOptionPane.showMessageDialog(this, "El campo de hora de regreso es obligatorio.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
                this.setCursor(Cursor.DEFAULT_CURSOR);
                return;
            }
        } 
        try {
            viaje_salida = buscarViaje(viajes.get(selectedIndex - 1).getId(), dt_fecha_salida, dt_hora_salida.toString() + ":00");            
            if (chk_redondo.isSelected()){
                Trip aux = buscarViaje(viajes.get(selectedIndex - 1).getId(), dt_fecha_regreso, dt_hora_regreso.toString() + ":00");
                if (viaje_salida != null && aux != null)
                    if (viaje_salida.getId() != aux.getId())
                        viaje_regreso = aux;
                    else {
                        viaje_regreso = null;
                        JOptionPane.showMessageDialog(this, "El viaje de salida no puede ser el mismo que el de regreso.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
                    }
                else 
                    viaje_regreso = null;
            }
            if (viaje_salida == null && viaje_regreso == null && chk_redondo.isSelected()){
                JOptionPane.showMessageDialog(this, "No hay viajes de salida ni de regreso en las fechas seleccionadas.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
            } else if (viaje_salida == null){
                JOptionPane.showMessageDialog(this, "No hay viajes de salida en las fechas seleccionadas.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
            } else if (viaje_regreso == null && chk_redondo.isSelected()) {
                JOptionPane.showMessageDialog(this, "No hay viajes de regreso en las fechas seleccionadas.", "Boleto", JOptionPane.INFORMATION_MESSAGE);
            }
            obtener_asientos();
        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        } finally{
            this.setCursor(Cursor.DEFAULT_CURSOR);
        }
    }//GEN-LAST:event_btnBuscarRegresoActionPerformed

