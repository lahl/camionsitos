    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(txtCorreo.getText().equals("") || txtDomicilio.getText().equals("") ||txtLicencia.getText().equals("") 
                || txtNombre.getText().equals("") || txtTelefono.getText().equals("") || dcApFin.getDate().equals("") 
                || dcApInicio.getDate().equals("") || dcContratoFin.getDate().equals("") || dcContratoInicio.getDate().equals("") 
                || dcNacimiento.getDate().equals("")){
            
            JOptionPane.showMessageDialog(null, "Error, campos de textos vacío", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            
            Driver d = new Driver();
            DriverP dp = new DriverP();

            d.setAddres(txtDomicilio.getText());
            d.setCel(txtTelefono.getText());
            d.setEmail(txtCorreo.getText());
            d.setLicense(txtLicencia.getText());
            d.setName(txtNombre.getText());
            d.setApEnd(dcApFin.getDate());
            d.setApStart(dcApInicio.getDate());
            d.setBirthDate(dcNacimiento.getDate());
            d.setContractDateStart(dcContratoInicio.getDate());
            d.setContractDateEnd(dcContratoFin.getDate());
        
            dp.addDriver(d, Sesion.getId());   
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: "+e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

