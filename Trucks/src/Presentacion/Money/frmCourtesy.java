     void ModelTable(){
        DefaultTableModel model= new DefaultTableModel();
        table=this.tblCourtesy;
        table.setModel(model);
        
        //Dont move columns
        table.getTableHeader().setReorderingAllowed(false);
        
        //Identificadores de las columnas de la tabla
            model.setColumnIdentifiers(new Object[]{"id","Código", "Disponible"});
            if (table.getColumnModel().getColumnCount() > 0){
            table.getColumnModel().getColumn(0).setMinWidth(0);
            table.getColumnModel().getColumn(0).setMaxWidth(0);
            
            table.getTableHeader().setFont(new Font("SansSerif", Font.BOLD,20));
            table.getColumnModel().getColumn(1).setPreferredWidth(250);
            table.getColumnModel().getColumn(2).setPreferredWidth(200);
            }
           
            
            List<Courtesy> listC = new ArrayList<Courtesy>();
            listC = Cp.getAll();
            
        
        try{
            for(int i=0; i<= listC.size()-1; i++){
                model.addRow(new Object[]{listC.get(i).getId(), listC.get(i).getCode(), listC.get(i).isAvlb()});
                
            }
            
        }catch(Exception e){
            System.out.println("Error en tabla Cortesía: "+e.getMessage());
            
        }
    }
    
