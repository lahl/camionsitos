    private void tblCourtesyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCourtesyMouseClicked
        if(evt.getButton() ==1){
            int fila = tblCourtesy.getSelectedRow();
            
            if(fila > -1){
               
                String id =(String.valueOf(tblCourtesy.getValueAt(fila, 0)));
                String code=(String.valueOf(tblCourtesy.getValueAt(fila, 1)));
                String bol =(String.valueOf(tblCourtesy.getValueAt(fila, 2)));
                
                
                c = new Courtesy();
                c.setId(Integer.parseInt(id));
                c.setCode(code);
                c.setAvlb(Boolean.parseBoolean(bol));
            
            }
        }
    }//GEN-LAST:event_tblCourtesyMouseClicked

    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        CourtesyP cp = new CourtesyP();
        Courtesy c = new Courtesy();
        
        String code = cp.genCode();
        boolean status = true;
        
        c.setAvlb(status);
        c.setCode(code);
        
        
        
        int res = JOptionPane.showConfirmDialog(null, "Estás apunto de crear una cortesía con los siguientes datos, puedes anotar \n"+"Código: "+code, "Cortesía", JOptionPane.YES_NO_OPTION);
        
        if(res==0){
            cp.genCourtesy(c, Sesion.getId());
            ModelTable();
        }
        
    }//GEN-LAST:event_btnEliminar1ActionPerformed

