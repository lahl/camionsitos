    private void tblBusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBusMouseClicked
        if(evt.getButton()==1){
            try {
                int fila = tblBus.getSelectedRow();            
            if(fila > -1){
                  int id = (Integer.valueOf(tblBus.getValueAt(fila, 0).toString()));
                  String origen = (String.valueOf(tblBus.getValueAt(fila, 1)));
                  String destino = (String.valueOf(tblBus.getValueAt(fila, 2)));                  
                  double precio = (Double.valueOf(tblBus.getValueAt(fila, 3).toString()));
                  r  = new Route();                  
                  r.setId(id);
                  r.setOrigin(origen);
                  r.setDestination(destino);                  
                  r.setPrice(precio);
            }
            }catch(NumberFormatException e){
               JOptionPane.showMessageDialog(null, "Error al editar la información, verifica que los datos sean correctos \n"+"Error: "+e, "Error", JOptionPane.ERROR_MESSAGE);
               
           }
                
        }
    }//GEN-LAST:event_tblBusMouseClicked

