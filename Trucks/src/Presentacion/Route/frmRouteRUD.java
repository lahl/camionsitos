    void ModelTableDiscount(String something){
        DefaultTableModel model = new DefaultTableModel();
        table=this.tblBus;
        table.setModel(model);
        
        
        model.setColumnIdentifiers(new Object[]{"id","Origen", "Destino", "Precio"});
            if (table.getColumnModel().getColumnCount() > 0)
            {
                //Hide ID Column
                table.getColumnModel().getColumn(0).setMinWidth(0);
                table.getColumnModel().getColumn(0).setMaxWidth(0);
                //Font headers
                table.getTableHeader().setFont(new Font("SansSerif", Font.BOLD,20));


                table.getColumnModel().getColumn(1).setPreferredWidth(200);
                table.getColumnModel().getColumn(2).setPreferredWidth(200);
                table.getColumnModel().getColumn(3).setPreferredWidth(100);
            
            }
            //database
            List<Route> listB = new ArrayList<Route>();
            List<Route> listC = new ArrayList<Route>();
            
            RouteP up = new RouteP();
            
            try{
                
                //Get All Users
                if( something == "null"){
                    listB = up.getAllRoutes();
                    for(int i = 0; i<= listB.size()-1; i++){
                         model.addRow(
                                 new Object[]{listB.get(i).getId(), listB.get(i).getOrigin(), 
                                     listB.get(i).getDestination(), listB.get(i).getPrice()});
                        
                    }
                }
                
                
                //Get some users                
                if( something.equals("something") ){
                    listC = up.getRoute(txtBuscarOrigin.getText().trim(), txtBuscarDestino.getText().trim());
                    for(int i = 0; i<= listC.size()-1; i++){
                         model.addRow(
                                 new Object[]{listC.get(i).getId(), listC.get(i).getOrigin(), 
                                     listC.get(i).getDestination(), listC.get(i).getPrice()});                        
                    }
                }

                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error al mostrar rutas \n"+ e.getMessage(), "ERRPR", JOptionPane.ERROR_MESSAGE);
            }
    }
    
    
