    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        // Modificar
        int fila = tblBus.getSelectedRow();            
           if(fila > -1){
            try {
                int id = (Integer.valueOf(tblBus.getValueAt(fila, 0).toString()));
                FrmFormulario formulario = new FrmFormulario(id);
                formulario.setModal(true);
                formulario.setLocationRelativeTo(this);
                formulario.show();
                switch(formulario.getResult().getValor()){
                    case 1:  
                        JOptionPane.showMessageDialog(this, "El viaje se ha guardado correctamente.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                        ModelTableDiscount();
                        break;//ok
                    case 3: 
                        JOptionPane.showMessageDialog(this, "Ocurri√≥ un error inesperado, por favor contacte a soporte t√©cnico.", "Mensaje", JOptionPane.ERROR_MESSAGE);
                        break;//error al ejecutar query                    
                    default:
                        break;
                }                  
            } catch (SQLException ex) {
                Logger.getLogger(frmTripRUD.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Error al editar la informaci√≥n, verifica que los datos sean correctos \n"+"Error: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(frmTripRUD.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Error al editar la informaci√≥n, verifica que los datos sean correctos \n"+"Error: "+ex, "Error", JOptionPane.ERROR_MESSAGE);
            }
           } else 
                JOptionPane.showMessageDialog(null, "Debe seleccionar un registro de la tabla.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btn_modificarActionPerformed

