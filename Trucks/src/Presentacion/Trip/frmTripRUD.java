    void ModelTableDiscount(){
        DefaultTableModel model = new DefaultTableModel();
        table=this.tblBus;
        table.setModel(model);
        model.setColumnIdentifiers(new Object[]{"ID", "Salida","Llegada", "Origen", "Destino", "Precio", "Asientos", "ASIS. D.", "Conductor", "Cami√≥n"});
        if (table.getColumnModel().getColumnCount() > 0)
        {
            //Hide ID Column
            table.getColumnModel().getColumn(0).setMinWidth(0);
            table.getColumnModel().getColumn(0).setMaxWidth(0);
            //Font headers
            table.getTableHeader().setFont(new Font("SansSerif", Font.BOLD,20));
            table.getColumnModel().getColumn(1).setPreferredWidth(120);//salida
            table.getColumnModel().getColumn(2).setPreferredWidth(120);//llegada
            table.getColumnModel().getColumn(3).setPreferredWidth(150);//origne
            table.getColumnModel().getColumn(4).setPreferredWidth(150);//destino
            table.getColumnModel().getColumn(5).setPreferredWidth(65);//precio
            table.getColumnModel().getColumn(6).setPreferredWidth(65);//asientos
            table.getColumnModel().getColumn(7).setPreferredWidth(65);//conductor
            table.getColumnModel().getColumn(8).setPreferredWidth(65);//camion
        }
        String dt_fecha_salida = dt_salida.getDatePicker().getComponentDateTextField().getText();
        String dt_hora_salida = dt_salida.getTimePicker().getComponentTimeTextField().getText();
        int selectedIndex = cbo_origen.getSelectedIndex();        
        List<TripTransporte> listB = new ArrayList();
        try{                
            //Get All Trips
            int id_viaje = 0;
            if (selectedIndex > 0)
                id_viaje = viajes.get(selectedIndex - 1).getId();
            if (!Funciones.isNullOrEmpty(dt_hora_salida))
                dt_hora_salida = dt_hora_salida + ":00";
            listB = TripNegocio.obtenerTodosTransporte(id_viaje, dt_fecha_salida, dt_hora_salida);
            for(int i = 0; i<= listB.size()-1; i++){
                 model.addRow(
                         new Object[]{listB.get(i).getId(), 
                             listB.get(i).getSalida(), 
                             listB.get(i).getLlegada(), 
                             listB.get(i).getOrigen(), 
                             listB.get(i).getDestino(), 
                             listB.get(i).getPrecio(),
                             listB.get(i).getAsientos(),
                             listB.get(i).getAsientosDisponibles(),
                             listB.get(i).getConductor(),
                             listB.get(i).getCamion() 
                         });
            }              
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al mostrar rutas \n"+ e.getMessage(), "ERRPR", JOptionPane.ERROR_MESSAGE);
        }   
    }
    
    private void llena_combo() throws SQLException, ClassNotFoundException{
        modeloOrigen.removeAllElements();              
        modeloOrigen.addElement("Seleccionar"); 
        viajes = TripNegocio.obtenerTodosCombo();
        if (viajes != null) {
            viajes.forEach((viaje) -> {
                modeloOrigen.addElement(viaje.getOrigin() + " - " + viaje.getDestination()); 
            });
        }
        cbo_origen.setModel(modeloOrigen);
    }   
    
