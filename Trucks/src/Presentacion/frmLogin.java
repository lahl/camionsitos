    private void login() {
        UserP up = new UserP();
        User u = up.login(txt_user.getText().trim(), txt_pass.getText().trim());
        if (u != null) { 
            Sesion.setId(u.getId());
            Sesion.setLevel(u.getLevel());
            if (u.getLevel().equals("Admin")){
                frmHome frmH = new frmHome();
                frmH.setVisible(true);
                this.dispose();
            } else if (u.getLevel().equals("Normal")) {
                try {
                    frmCaja frmH = new frmCaja();
                    frmH.setVisible(true);
                    this.dispose();
                } catch (SQLException ex) {
                    Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "El usuario o contraseña son incorrectos", "Inicio de Sesión", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
